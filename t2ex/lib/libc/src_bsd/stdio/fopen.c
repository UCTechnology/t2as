#include <sys/types.h>
#include <sys/stat.h>
#include <fcntl.h>
#include <limits.h>
#include <stdio.h>
#include <errno.h>
#include <unistd.h>
#include "local.h"

FILE *
fopen_eno(const char *file, const char *mode, int *eno)
{
	FILE *fp;
	int f;
	int flags, oflags;

	if ((flags = __sflags(mode, &oflags)) == 0)
		{ f = EINVAL; goto e1; }
	if ((fp = __sfp()) == NULL)
		{ f = EMFILE; goto e1; }
	if ((f = __libc_open(file, oflags, DEFFILEMODE)) < 0) {
		fp->_flags = 0;			/* release */
		f &= 0xffff; goto e1;
	}

	/* _file is only a short */
	if (f > SHRT_MAX) {
		fp->_flags = 0;			/* release */
		__libc_close(f);
		/*errno*/f = EMFILE;
		goto e1;
	}

	fp->_file = f;
	fp->_flags = flags;
	fp->_cookie = fp;
	fp->_read = __sread;
	fp->_write = __swrite;
	fp->_seek = __sseek;
	fp->_close = __sclose;

	/*
	 * When opening in append mode, even though we use O_APPEND,
	 * we need to seek to the end so that ftell() gets the right
	 * answer.  If the user then alters the seek pointer, or
	 * the file extends, this will fail, but there is not much
	 * we can do about this.  (We could set __SAPP and check in
	 * fseek and ftell.)
	 */
	if (oflags & O_APPEND)
		(void) __sseek((void *)fp, (fpos_t)0, SEEK_END);
	return (fp); e1: if (eno) *eno = f; return NULL;
}
FILE *fopen(const char *path, const char *mode){ return fopen_eno(path, mode, NULL); }
